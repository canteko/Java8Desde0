public class TiposDeDatos {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		// Boolean
		boolean isTrue = true;
		// Caracteres
		String sentence = "Hola mundo";
		char caracter = 'a';
		// Numerics
		byte byteVal = 100;
		short shortVal = 1000;
		int intVal = 1232;
		// Valor 26 en...
		int decimalVal = 26;
		int hexadecimalVal = 0x1a;
		int binaryVal = 0b11010;
		
		// Tarjeta de credito, los _ son ignorados
		long creditCardNumber = 1234_5678_9101_1121L;
	}

}
